#include <iostream>
using namespace std;

int main(){
    int x = 10;
    int y = 11;

    cout <<"Is equal: "<< (x==y) << endl;
    cout <<"Not equal: "<< (x!=y) << endl;
    cout <<"Is X > Y: "<< (x>y) << endl;
    cout <<"Is X < Y: "<< (x<y) << endl;
    cout <<"Is X >= y: "<<(x>=y) << endl;
    cout <<"Is X <= Y: "<<(x<=y) << endl;

    return 0;
}
